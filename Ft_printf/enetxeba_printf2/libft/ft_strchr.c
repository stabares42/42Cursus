#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (c & 0xFF))
			return ((char *) s);
		s++;
	}
	if ((c & 0xFF) == '\0')
		return ((char *) s);
	return (0);
}
